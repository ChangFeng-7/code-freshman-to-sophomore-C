#include <iostream>
#include <stdio.h>
using namespace std;
int main(){
    char n,m;
    char a[100],NUM;
    int num=true;
    int i=0;
    scanf("%c",&n);
    while(1){
        a[i]=n;
        scanf("%c",&n);
        if(n=='\n'&&a[i]=='.') break;
        i++;
    }
    int j;
    for(j=0;j<i;j++){
        if(a[j]=='('){
            for(int k=j+1;k<i;k++){
                if(a[k]==')') break;
                else{ num=false; NUM=')'; }
            }
        }
        if(a[j]=='['){
            for(int k=j+1;k<i;k++){
                if(a[k]==']') break;
                else{ num=false; NUM=']';}
            }
        }
        if(a[j]=='{'){
            for(int k=j+1;k<i;k++){
                if(a[k]=='}') break;
                else{ num=false; NUM='}';}
            }
        }
        if(a[j]=='/'){
            for(int k=j+1;k<i;k++){
                if(a[k]=='/') break;
                else{ num=false; NUM='/';}
            }
        }
    }
    if(num==true){
        cout<<"YES\n";
    }else{
        cout<<"NO\n"<<"?-"<<NUM;
    }
    return 0;
}
